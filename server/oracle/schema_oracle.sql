-- ============================================================
-- Function Requirement 1: Hazard count for an OFFER listing
-- ============================================================
CREATE OR REPLACE FUNCTION fn_offer_hazard_count (
  p_offer_listing_id IN NUMBER
) RETURN NUMBER
IS
  v_cnt NUMBER := 0;
BEGIN
  SELECT COUNT(*)
  INTO v_cnt
  FROM batch_composition mc1
  JOIN batch_composition mc2
    ON mc1.listing_id = mc2.listing_id
   AND mc1.chem_id < mc2.chem_id
  JOIN hazard_matrix hm
    ON hm.chem_id_1 = mc1.chem_id
   AND hm.chem_id_2 = mc2.chem_id
   AND hm.is_incompatible = 1
  WHERE mc1.listing_id = p_offer_listing_id;

  RETURN NVL(v_cnt, 0);
END;
/

-- ============================================================
-- Function Requirement 2: Pair compatibility using explicit CURSOR
-- Returns 1 when OFFER meets all DEMAND criteria, else 0
-- ============================================================
CREATE OR REPLACE FUNCTION fn_pair_criteria_match (
  p_offer_listing_id  IN NUMBER,
  p_demand_listing_id IN NUMBER
) RETURN NUMBER
IS
  CURSOR cur_criteria IS
    SELECT chem_id, min_percentage, max_percentage
    FROM acceptance_criteria
    WHERE listing_id = p_demand_listing_id;

  v_offer_pct batch_composition.percentage%TYPE;
  v_chem_id   acceptance_criteria.chem_id%TYPE;
  v_min       acceptance_criteria.min_percentage%TYPE;
  v_max       acceptance_criteria.max_percentage%TYPE;
BEGIN
  OPEN cur_criteria;
  LOOP
    FETCH cur_criteria INTO v_chem_id, v_min, v_max;
    EXIT WHEN cur_criteria%NOTFOUND;

    BEGIN
      SELECT percentage
      INTO v_offer_pct
      FROM batch_composition
      WHERE listing_id = p_offer_listing_id
        AND chem_id = v_chem_id;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        CLOSE cur_criteria;
        RETURN 0;
    END;

    IF (v_min IS NOT NULL AND v_offer_pct < v_min)
       OR (v_max IS NOT NULL AND v_offer_pct > v_max)
    THEN
      CLOSE cur_criteria;
      RETURN 0;
    END IF;
  END LOOP;
  CLOSE cur_criteria;

  RETURN 1;
END;
/

-- ============================================================
-- Procedure Requirement 1: Initialize decision row
-- ============================================================
CREATE OR REPLACE PROCEDURE sp_create_offer_decision (
  p_offer_listing_id  IN NUMBER,
  p_demand_listing_id IN NUMBER
)
IS
  v_offer_owner  NUMBER;
  v_demand_owner NUMBER;
  v_exists       NUMBER;
BEGIN
  SELECT user_id INTO v_offer_owner
  FROM listings
  WHERE id = p_offer_listing_id
    AND type = 'OFFER'
    AND status = 'ACTIVE';

  SELECT user_id INTO v_demand_owner
  FROM listings
  WHERE id = p_demand_listing_id
    AND type = 'DEMAND'
    AND status = 'ACTIVE';

  IF fn_pair_criteria_match(p_offer_listing_id, p_demand_listing_id) = 0 THEN
    RAISE_APPLICATION_ERROR(-20010, 'Offer and demand pair is not criteria-compatible.');
  END IF;

  SELECT COUNT(*) INTO v_exists
  FROM offer_decisions
  WHERE offer_listing_id = p_offer_listing_id
    AND demand_listing_id = p_demand_listing_id;

  IF v_exists = 0 THEN
    INSERT INTO offer_decisions (
      offer_listing_id,
      demand_listing_id,
      seller_user_id,
      buyer_user_id,
      seller_decision,
      buyer_decision,
      final_status
    ) VALUES (
      p_offer_listing_id,
      p_demand_listing_id,
      v_offer_owner,
      v_demand_owner,
      'PENDING',
      'PENDING',
      'OPEN'
    );
  END IF;
END;
/

-- ============================================================
-- Procedure Requirement 2: Seller/Buyer response finalization
-- - both ACCEPTED => close both listings
-- - any REJECTED  => keep ACTIVE and mark REJECTED
-- ============================================================
CREATE OR REPLACE PROCEDURE sp_respond_offer_decision (
  p_offer_listing_id  IN NUMBER,
  p_demand_listing_id IN NUMBER,
  p_actor_user_id     IN NUMBER,
  p_decision          IN VARCHAR2
)
IS
  v_decision      VARCHAR2(10);
  v_row           offer_decisions%ROWTYPE;
  v_next_seller   VARCHAR2(10);
  v_next_buyer    VARCHAR2(10);
  v_next_status   VARCHAR2(10);
BEGIN
  v_decision := UPPER(TRIM(p_decision));

  IF v_decision NOT IN ('ACCEPTED', 'REJECTED') THEN
    RAISE_APPLICATION_ERROR(-20020, 'Decision must be ACCEPTED or REJECTED.');
  END IF;

  SELECT * INTO v_row
  FROM offer_decisions
  WHERE offer_listing_id = p_offer_listing_id
    AND demand_listing_id = p_demand_listing_id
  FOR UPDATE;

  IF v_row.final_status <> 'OPEN' THEN
    RAISE_APPLICATION_ERROR(-20021, 'Decision workflow is already finalized.');
  END IF;

  IF p_actor_user_id = v_row.seller_user_id THEN
    v_next_seller := v_decision;
    v_next_buyer := v_row.buyer_decision;
  ELSIF p_actor_user_id = v_row.buyer_user_id THEN
    v_next_seller := v_row.seller_decision;
    v_next_buyer := v_decision;
  ELSE
    RAISE_APPLICATION_ERROR(-20022, 'Actor is neither seller nor buyer for this pair.');
  END IF;

  IF v_next_seller = 'REJECTED' OR v_next_buyer = 'REJECTED' THEN
    v_next_status := 'REJECTED';
  ELSIF v_next_seller = 'ACCEPTED' AND v_next_buyer = 'ACCEPTED' THEN
    v_next_status := 'ACCEPTED';
  ELSE
    v_next_status := 'OPEN';
  END IF;

  UPDATE offer_decisions
  SET seller_decision = v_next_seller,
      buyer_decision = v_next_buyer,
      final_status = v_next_status
  WHERE id = v_row.id;

  IF v_next_status = 'ACCEPTED' THEN
    UPDATE listings
    SET status = 'CLOSED', updated_at = SYSTIMESTAMP
    WHERE id IN (p_offer_listing_id, p_demand_listing_id)
      AND status = 'ACTIVE';
  END IF;
END;
/

