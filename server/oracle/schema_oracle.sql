-- ============================================================
-- Core Tables
-- ============================================================
CREATE TABLE users (
  id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name          VARCHAR2(120) NOT NULL,
  email         VARCHAR2(160) NOT NULL UNIQUE,
  password_hash VARCHAR2(300) NOT NULL,
  industry_type VARCHAR2(80),
  location      VARCHAR2(120),
  created_at    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE chemicals (
  id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code         VARCHAR2(40) NOT NULL UNIQUE,
  name         VARCHAR2(120) NOT NULL,
  hazard_level VARCHAR2(10) DEFAULT 'LOW' NOT NULL,
  CONSTRAINT chk_chem_hazard_level CHECK (hazard_level IN ('LOW', 'MEDIUM', 'HIGH'))
);

CREATE TABLE listings (
  id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       NUMBER NOT NULL,
  type          VARCHAR2(10) NOT NULL,
  material_name VARCHAR2(180) NOT NULL,
  quantity_tons NUMBER(12, 2) NOT NULL,
  status        VARCHAR2(12) DEFAULT 'ACTIVE' NOT NULL,
  created_at    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT chk_listing_type CHECK (type IN ('OFFER', 'DEMAND')),
  CONSTRAINT chk_listing_status CHECK (status IN ('ACTIVE', 'CLOSED', 'CANCELLED')),
  CONSTRAINT fk_listings_user FOREIGN KEY (user_id) REFERENCES users(id)
);

CREATE TABLE batch_composition (
  id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  listing_id    NUMBER NOT NULL,
  chem_id       NUMBER NOT NULL,
  percentage    NUMBER(6, 2) NOT NULL,
  CONSTRAINT chk_batch_percentage CHECK (percentage >= 0 AND percentage <= 100),
  CONSTRAINT uq_listing_chem UNIQUE (listing_id, chem_id),
  CONSTRAINT fk_batch_listing FOREIGN KEY (listing_id) REFERENCES listings(id) ON DELETE CASCADE,
  CONSTRAINT fk_batch_chemical FOREIGN KEY (chem_id) REFERENCES chemicals(id)
);

CREATE TABLE acceptance_criteria (
  id             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  listing_id     NUMBER NOT NULL,
  chem_id        NUMBER NOT NULL,
  min_percentage NUMBER(6,2),
  max_percentage NUMBER(6,2),
  CONSTRAINT chk_criteria_range CHECK (
    (min_percentage IS NULL OR (min_percentage >= 0 AND min_percentage <= 100))
    AND (max_percentage IS NULL OR (max_percentage >= 0 AND max_percentage <= 100))
    AND (min_percentage IS NULL OR max_percentage IS NULL OR min_percentage <= max_percentage)
  ),
  CONSTRAINT uq_criteria_listing_chem UNIQUE (listing_id, chem_id),
  CONSTRAINT fk_criteria_listing FOREIGN KEY (listing_id) REFERENCES listings(id) ON DELETE CASCADE,
  CONSTRAINT fk_criteria_chemical FOREIGN KEY (chem_id) REFERENCES chemicals(id)
);

CREATE TABLE hazard_matrix (
  id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  chem_id_1       NUMBER NOT NULL,
  chem_id_2       NUMBER NOT NULL,
  is_incompatible NUMBER(1) DEFAULT 1 NOT NULL,
  notes           VARCHAR2(250),
  CONSTRAINT chk_hazard_incompat CHECK (is_incompatible IN (0,1)),
  CONSTRAINT uq_hazard_pair UNIQUE (chem_id_1, chem_id_2),
  CONSTRAINT fk_hazard_chem_1 FOREIGN KEY (chem_id_1) REFERENCES chemicals(id),
  CONSTRAINT fk_hazard_chem_2 FOREIGN KEY (chem_id_2) REFERENCES chemicals(id)
);

CREATE TABLE process_capabilities (
  id                    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  processor_id          NUMBER NOT NULL,
  input_chem_id         NUMBER NOT NULL,
  output_chem_id        NUMBER NOT NULL,
  conversion_efficiency NUMBER(5,2) NOT NULL,
  CONSTRAINT chk_efficiency CHECK (conversion_efficiency > 0 AND conversion_efficiency <= 100),
  CONSTRAINT fk_proc_user FOREIGN KEY (processor_id) REFERENCES users(id),
  CONSTRAINT fk_proc_input FOREIGN KEY (input_chem_id) REFERENCES chemicals(id),
  CONSTRAINT fk_proc_output FOREIGN KEY (output_chem_id) REFERENCES chemicals(id)
);

CREATE TABLE offer_decisions (
  id                NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  offer_listing_id  NUMBER NOT NULL,
  demand_listing_id NUMBER NOT NULL,
  seller_user_id    NUMBER NOT NULL,
  buyer_user_id     NUMBER NOT NULL,
  seller_decision   VARCHAR2(10) DEFAULT 'PENDING' NOT NULL,
  buyer_decision    VARCHAR2(10) DEFAULT 'PENDING' NOT NULL,
  final_status      VARCHAR2(10) DEFAULT 'OPEN' NOT NULL,
  created_at        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT chk_seller_decision CHECK (seller_decision IN ('PENDING', 'ACCEPTED', 'REJECTED')),
  CONSTRAINT chk_buyer_decision CHECK (buyer_decision IN ('PENDING', 'ACCEPTED', 'REJECTED')),
  CONSTRAINT chk_final_status CHECK (final_status IN ('OPEN', 'ACCEPTED', 'REJECTED', 'CANCELLED')),
  CONSTRAINT uq_offer_demand_pair UNIQUE (offer_listing_id, demand_listing_id),
  CONSTRAINT fk_offer_dec_offer FOREIGN KEY (offer_listing_id) REFERENCES listings(id) ON DELETE CASCADE,
  CONSTRAINT fk_offer_dec_demand FOREIGN KEY (demand_listing_id) REFERENCES listings(id) ON DELETE CASCADE,
  CONSTRAINT fk_offer_dec_seller FOREIGN KEY (seller_user_id) REFERENCES users(id),
  CONSTRAINT fk_offer_dec_buyer FOREIGN KEY (buyer_user_id) REFERENCES users(id)
);

